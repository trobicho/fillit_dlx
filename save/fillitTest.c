void	p_sol(t_fifo *fifo)
{
	t_qlist *list;
	int i = 0, j;
	int w = 6, h = 6, nbPiece = 4;
	char **tab = malloc(sizeof(char*) * h);
	char name;

	while (i < h)
	{
		tab[i] = malloc(sizeof(char) * w);
		j = 0;
		while (j < w)
			tab[i][j++] = '.';
		i++;
	}

	while (fifo != NULL)
	{
		name = (char)(fifo->lst->clh->name + 'A');
		list = fifo->lst->r;
		while (list != fifo->lst)
		{
			tab[(list->clh->name-nbPiece) / h][(list->clh->name-nbPiece) % w] = name;
			list = list->r;
			i++;
		}
		fifo = fifo->next;
	}

	i = 0;
	while (i < h)
	{
		j = 0;
		while (j < w)
			printf("%c", tab[i][j++]);
		printf("\n");
		i++;
	}
	printf("\n");
	n_sol++;
}

